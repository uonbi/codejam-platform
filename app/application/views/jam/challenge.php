<h3><i class="fa fa-code"></i> Main Challenge</h3>
<h4>Kitakale Kingdom Break-in</h4>
<p>Bob and his army are planning to attack the great Kitakale kingdom. King Mamba 
	learns of this conspiracy
	and plans to lock the only door through which Bob's army 
	can enter his Kingdom. But to lock the door
	King Mamba needs a key that is a <a href="http://en.wikipedia.org/wiki/Palindrome" target="_blank">
	palindrome</a> of a certain 
	<a href="http://en.wikipedia.org/wiki/Anagram" target="_blank">
		anagram</a> string. He has a bag which holds all
	the keys to the Kingdom's doors composed of lowercase 
	English letters.
</p>

<p>A key 'aaabbbb' would open the door since an anagram of the key would 
	be 'bbaaabb' which is a palindrome. Another anagram of the 
	same key('aaabbbb') would be 'abbabba' which would open 
	the door as well. The key 'aaabbbb' is a valid key and is 
	considered only once even though it has more than one palindrome.
</p>
<p>
	The file <a href="<?php echo base_url();?>files/key_bag.txt" download>key_bag.txt</a> 
	contains over 10,000 keys to the Kingdom. Find the probability 
	that King Mamba will pick an Invalid key in the first instance. 
	Give your answer correct to ten(10) decimal places.
</p>

<h3><i class="fa fa-code"></i> Supplimentary Challenge</h3>
<p>The number 2401 is equal to the sum of its digits raised to 
	some power: 2 + 4 + 0 + 1 = 7, and 7<sup>4</sup> = 2401. 
	Another example of a number with this property is 
	5832 = 18<sup>3</sup>.
</p>
<p>We shall define <strong>a(n)</strong> to be the nth term of 
	this sequence and insist that <strong>a</strong> number 
	must contain at least two digits to have a sum.
</p>
<p>You are given that a(3) = 2401, a(5) = 5832 and 
	a(12) = 34012224. Find the last 20 digits in a(79).</p>

